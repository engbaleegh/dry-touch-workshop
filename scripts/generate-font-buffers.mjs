import fs from "fs";
import path from "path";

const dir = path.join(process.cwd(), "src/lib/pdf/fonts");
const files = [
  "NotoSansArabic-Regular.ttf",
  "Roboto-Regular.ttf",
  "Roboto-Medium.ttf",
  "Roboto-Italic.ttf",
  "Roboto-MediumItalic.ttf",
];

let out = "// Auto-generated by scripts/generate-font-buffers.mjs — do not edit manually\n\n";

for (const file of files) {
  const buffer = fs.readFileSync(path.join(dir, file));
  const key = file.replace(/[-.]/g, "_").toUpperCase();
  out += `export const ${key} = Buffer.from(${JSON.stringify(buffer.toString("base64"))}, "base64");\n\n`;
}

const target = path.join(process.cwd(), "src/lib/pdf/font-buffers.ts");
fs.writeFileSync(target, out);
console.log("Wrote", target, fs.statSync(target).size, "bytes");
